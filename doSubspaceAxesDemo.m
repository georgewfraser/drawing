clf;
colormap(gray);
[x,y] = meshgrid(-1:.1:1);
z = x;
ax = [1 0 1; 0 1 0];
ax = bsxfun(@rdivide,ax,sqrt(sum(ax.^2,2)));

subplot(1,3,1);
xlim([-1 1]);
ylim([-1 1]);
zlim([-1 1]);
surf(x,y,z);
bumpax = bsxfun(@plus,ax,[0 0 .05]);
arrow([0 0 0],bumpax(1,:),25,'Width',5,'FaceColor',[1 0 0],'EdgeColor',[1 0 0]);
arrow([0 0 0],bumpax(2,:),25,'Width',5,'FaceColor',[0 1 0],'EdgeColor',[0 1 0]);
axis image;

subplot(1,3,2);
xlim([-1 1]);
ylim([-1 1]);
zlim([-1 1]);
rot = eye(3,2)*orth(rand(2))*[1 0 1; 0 1 0];
ax = ax*rot;
surf(x,y,z);
bumpax = bsxfun(@plus,ax,[0 0 .05]);
arrow([0 0 0],bumpax(1,:),25,'Width',5,'FaceColor',[1 0 0],'EdgeColor',[1 0 0]);
arrow([0 0 0],bumpax(2,:),25,'Width',5,'FaceColor',[0 1 0],'EdgeColor',[0 1 0]);
axis image;

subplot(1,3,3);
xlim([-1 1]);
ylim([-1 1]);
zlim([-1 1]);
rot = eye(3,2)*orth(rand(2))*[1 0 1; 0 1 0];
ax = ax*rot;
surf(x,y,z);
bumpax = bsxfun(@plus,ax,[0 0 .05]);
arrow([0 0 0],bumpax(1,:),25,'Width',5,'FaceColor',[1 0 0],'EdgeColor',[1 0 0]);
arrow([0 0 0],bumpax(2,:),25,'Width',5,'FaceColor',[0 1 0],'EdgeColor',[0 1 0]);
axis image;